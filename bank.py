


# This code prompts the user to enter a greeting and then checks if the input matches any of the
# specified greetings. If the input matches "Hello" or "Hello, Gloria", it prints "0". If the input
# matches "How are you doing", it prints "20". Otherwise, it prints "100".
customer = input("greetings ")


if customer == "Hello":
   print("0")
elif customer == "Hello, Gloria":
    print("0")
elif customer == "How are you doing":
    print("20")
else:
    print("100")